import java.util.Scanner;

public class S_03 {
    public static void main(String[] args) {


        int carpim = 1;

        int toplam = 0;

        if(5<=3) {
            for(int i=5; i<=3; i++) {
                toplam = toplam + i;
            }
        } else {
            for(int i=5; i>=3; i--) {
                carpim = carpim * i;
            }
        }
        System.out.print(carpim);
        System.out.println(toplam);

    }




    }

